export default defineEventHandler((event) => {
  // Получаем категорию из query параметров
  const { category } = getQuery(event);
  
  // Если категория не указана, возвращаем все рекомендации
  if (!category) {
    return recommendations;
  }
  
  // Если категория указана, возвращаем рекомендации только для этой категории
  if (recommendations[category]) {
    return { [category]: recommendations[category] };
  } else {
    event.node.res.statusCode = 404;
    return {
      error: 'Категория не найдена'
    };
  }
}); 